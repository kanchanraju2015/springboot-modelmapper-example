package com.briz.springbootmodelmapperexample;


public class BookDTO
{
int id;
String aname;
String bname;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getAname() {
	return aname;
}
public void setAname(String aname) {
	this.aname = aname;
}
public String getBname() {
	return bname;
}
public void setBname(String bname) {
	this.bname = bname;
}
}
